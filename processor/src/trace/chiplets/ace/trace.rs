#[cfg(feature = "arbitrary")]
mod arbitrary {
    use proptest::{collection, prelude::*};

    use super::*;

    const MAX_TEST_NODES: usize = 8;

    fn arb_felt() -> impl Strategy<Value = Felt> {
        any::<u32>().prop_map(Felt::from_u32)
    }

    fn arb_quad_felt() -> impl Strategy<Value = QuadFelt> {
        (arb_felt(), arb_felt()).prop_map(|(c0, c1)| QuadFelt::new([c0, c1]))
    }

    impl Arbitrary for ReadNode {
        type Parameters = ();
        type Strategy = BoxedStrategy<Self>;

        fn arbitrary_with(_args: Self::Parameters) -> Self::Strategy {
            (arb_felt(), arb_felt(), arb_quad_felt(), arb_felt(), arb_quad_felt())
                .prop_map(|(ptr, id_0, v_0, id_1, v_1)| Self { ptr, id_0, v_0, id_1, v_1 })
                .boxed()
        }
    }

    impl Arbitrary for EvalNode {
        type Parameters = ();
        type Strategy = BoxedStrategy<Self>;

        fn arbitrary_with(_args: Self::Parameters) -> Self::Strategy {
            (
                arb_felt(),
                arb_felt(),
                arb_felt(),
                arb_quad_felt(),
                arb_felt(),
                arb_quad_felt(),
                arb_felt(),
                arb_quad_felt(),
            )
                .prop_map(|(ptr, eval_op, id_0, v_0, id_1, v_1, id_2, v_2)| Self {
                    ptr,
                    eval_op,
                    id_0,
                    v_0,
                    id_1,
                    v_1,
                    id_2,
                    v_2,
                })
                .boxed()
        }
    }

    impl Arbitrary for WireBus {
        type Parameters = ();
        type Strategy = BoxedStrategy<Self>;

        fn arbitrary_with(_args: Self::Parameters) -> Self::Strategy {
            (1usize..=MAX_TEST_NODES)
                .prop_flat_map(|wire_count| {
                    (
                        arb_felt(),
                        collection::vec((arb_quad_felt(), any::<u32>()), wire_count),
                        Just(wire_count as u32),
                    )
                })
                .prop_map(|(id_next, wires, num_wires)| Self { id_next, wires, num_wires })
                .boxed()
        }
    }

    impl Arbitrary for CircuitEvaluation {
        type Parameters = ();
        type Strategy = BoxedStrategy<Self>;

        fn arbitrary_with(_args: Self::Parameters) -> Self::Strategy {
            (0usize..=3, 1usize..=MAX_TEST_NODES)
                .prop_filter(
                    "ACE test circuit must fit the wire limit",
                    |(read_count, eval_count)| {
                        read_count.saturating_mul(2).saturating_add(*eval_count)
                            <= MAX_NUM_ACE_WIRES as usize
                    },
                )
                .prop_flat_map(|(read_count, eval_count)| {
                    let wire_count = read_count * 2 + eval_count;
                    (
                        any::<ContextId>(),
                        any::<u32>().prop_map(RowIndex::from),
                        collection::vec(any::<ReadNode>(), read_count),
                        collection::vec(any::<EvalNode>(), eval_count),
                        collection::vec((arb_quad_felt(), any::<u32>()), wire_count),
                        Just(wire_count as u32),
                    )
                })
                .prop_map(|(ctx, clk, read_nodes, eval_nodes, wires, num_wires)| Self {
                    ctx,
                    clk,
                    wire_bus: WireBus {
                        id_next: Felt::from_u32(num_wires),
                        wires,
                        num_wires,
                    },
                    read_nodes,
                    eval_nodes,
                })
                .boxed()
        }
    }
}

#[cfg(test)]
mod serialization_tests {
    use alloc::vec;

    use super::*;

    fn sample_read_node(value: QuadFelt) -> ReadNode {
        ReadNode {
            ptr: Felt::ZERO,
            id_0: Felt::ZERO,
            v_0: value,
            id_1: Felt::ONE,
            v_1: value,
        }
    }

    fn sample_eval_node(value: QuadFelt) -> EvalNode {
        EvalNode {
            ptr: Felt::ZERO,
            eval_op: Felt::ZERO,
            id_0: Felt::ZERO,
            v_0: value,
            id_1: Felt::ZERO,
            v_1: value,
            id_2: Felt::ONE,
            v_2: value,
        }
    }

    #[test]
    fn circuit_evaluation_read_rejects_mismatched_wire_bus_count() {
        let value = QuadFelt::new([Felt::ONE, Felt::ZERO]);
        let evaluation = CircuitEvaluation {
            ctx: ContextId::from(0),
            clk: RowIndex::from(0_u32),
            wire_bus: WireBus {
                id_next: Felt::ZERO,
                wires: vec![(value, 0), (value, 0)],
                num_wires: 2,
            },
            read_nodes: vec![sample_read_node(value)],
            eval_nodes: vec![sample_eval_node(value)],
        };

        let err = CircuitEvaluation::read_from_bytes(&evaluation.to_bytes()).unwrap_err();
        let DeserializationError::InvalidValue(message) = err else {
            panic!("expected invalid ACE wire count error");
        };
        assert!(message.contains("does not match read/eval node wire count"));
    }

    #[test]
    fn circuit_evaluation_read_rejects_empty_eval_section() {
        let value = QuadFelt::new([Felt::ONE, Felt::ZERO]);
        let evaluation = CircuitEvaluation {
            ctx: ContextId::from(0),
            clk: RowIndex::from(0_u32),
            wire_bus: WireBus {
                id_next: Felt::ZERO,
                wires: vec![(value, 0), (value, 0)],
                num_wires: 2,
            },
            read_nodes: vec![sample_read_node(value)],
            eval_nodes: Vec::new(),
        };

        let err = CircuitEvaluation::read_from_bytes(&evaluation.to_bytes()).unwrap_err();
        let DeserializationError::InvalidValue(message) = err else {
            panic!("expected invalid ACE eval section error");
        };
        assert!(message.contains("at least one eval node"));
    }
}
